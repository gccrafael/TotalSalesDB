DROP DATABASE IF EXISTS SALESDB;

CREATE DATABASE SALESDB;
USE SALESDB;

CREATE TABLE LOCATION (
        LOCATION_ID             INT,
        REGIONAL_GROUP          VARCHAR(20));

CREATE TABLE DEPARTMENT (
        DEPARTMENT_ID           INT,
        NAME                    VARCHAR(14),
        LOCATION_ID             INT);

CREATE TABLE JOB (
        JOB_ID                  INT,
        FUNCTION                VARCHAR(30));

CREATE TABLE EMPLOYEE (
        EMPLOYEE_ID             INT,
        LAST_NAME               VARCHAR(15),
        FIRST_NAME              VARCHAR(15),
        MIDDLE_INITIAL          VARCHAR(1),
        JOB_ID                  INT,
        MANAGER_ID              INT,
        HIRE_DATE               DATE,
        SALARY                  DOUBLE,
        COMMISSION              DOUBLE,
        DEPARTMENT_ID           INT);

CREATE TABLE SALARY_GRADE (
        GRADE_ID                INT,
        LOWER_BOUND             DOUBLE,
        UPPER_BOUND             DOUBLE);

CREATE TABLE PRODUCT (
        PRODUCT_ID              INT,
        DESCRIPTION             VARCHAR (30));
 
CREATE TABLE PRICE (
        PRODUCT_ID              INT,
        LIST_PRICE              DOUBLE,
        MIN_PRICE               DOUBLE,
        START_DATE              DATE,
        END_DATE                DATE);

CREATE TABLE CUSTOMER (
        CUSTOMER_ID             INT,
        NAME                    VARCHAR (45),
        ADDRESS                 VARCHAR (40),
        CITY                    VARCHAR (30),
        STATE                   VARCHAR (2),
        ZIP_CODE                VARCHAR (9),
        AREA_CODE               INT,
        PHONE_NUMBER            DECIMAL(7),
        SALESPERSON_ID          INT,
        CREDIT_LIMIT            DOUBLE,
        COMMENTS                VARCHAR(250));

CREATE TABLE SALES_ORDER  (
        ORDER_ID                INT,
        ORDER_DATE              DATE,
        CUSTOMER_ID             INT,
        SHIP_DATE               DATE,
        TOTAL                   DOUBLE);

CREATE TABLE ITEM  (
        ORDER_ID                INT,
        ITEM_ID                 INT,
        PRODUCT_ID              INT,
        ACTUAL_PRICE            DOUBLE,
        QUANTITY                INT,
        TOTAL                   DOUBLE);

